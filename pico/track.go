type Track struct {
	Name     string
	Album    string
	Artist   string
	Art      []int
	Length   time.Duration
	progress time.Time
}

func (t *Track) GetProgress() time.Time {
	return t.progress
}

func (t *Track) GetProgressFloat() float32 {
	return float32(time.Since(t.progress)) / float32(t.Length)
}

func (t *Track) SetProgress(time time.Time) {
	t.progress = time
}

func (t *Track) SetArt(art []int) {
	t.Art = art
}

func NewTrack(name, album, artist string, length time.Duration) Track {
	return Track{
		Name:     name,
		Album:    album,
		Artist:   artist,
		Art:      EmptyAlbumArt,
		Length:   length,
		progress: time.Now(),
	}
}
